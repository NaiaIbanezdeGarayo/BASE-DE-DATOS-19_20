/*OBTENER EL APELLIDO DE LOS EMPLEADOS 
CON EL MISMO OFICIO Y SALARIO QUE 'GIL'*/
SELECT APELLIDO, OFICIO, SALARIO
FROM EMPLE
WHERE (OFICIO,SALARIO) IN (SELECT OFICIO, SALARIO
                FROM EMPLE
                WHERE UPPER(APELLIDO) = 'GIL')
AND UPPER(APELLIDO)!= 'GIL';

SELECT APELLIDO, OFICIO, SALARIO
FROM EMPLE
WHERE OFICIO IN (SELECT OFICIO
                FROM EMPLE
                WHERE UPPER(APELLIDO) = 'GIL')
AND SALARIO IN (SELECT SALARIO
                FROM EMPLE
                WHERE UPPER(APELLIDO)= 'GIL')
AND UPPER(APELLIDO)!= 'GIL';
                
