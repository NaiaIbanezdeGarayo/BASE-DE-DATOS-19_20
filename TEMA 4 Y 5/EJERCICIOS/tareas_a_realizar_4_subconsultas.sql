/*1. Obtener el apellido 
de los empleados que tienen el mismo oficio que ‘GIL’.*/

SELECT OFICIO, APELLIDO
FROM EMPLE
WHERE UPPER(APELLIDO) = 'GIL';

SELECT APELLIDO
FROM EMPLE
WHERE OFICIO IN (SELECT DISTINCT OFICIO
                FROM EMPLE
                WHERE UPPER(APELLIDO) = 'GIL')
AND UPPER(APELLIDO)!= 'GIL';
                
