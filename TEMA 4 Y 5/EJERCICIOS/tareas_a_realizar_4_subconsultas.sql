/*2. Queremos consultar los datos de los empleados 
que trabajan en las localidades de ‘MADRID’ o ‘BARCELONA’.*/
SELECT E.EMP_NO, E.APELLIDO, E.OFICIO, E.ID_JEFE, E.FECHA_ALTA, 
E.SALARIO, E.COMISION_PCT, E.DEPT_NO, D.LOC
FROM EMPLE E, DEPART D
WHERE E.DEPT_NO = D.DEPT_NO --JOIN
AND UPPER(D.LOC) IN ('BARCELONA', 'MADRID');
 
