--EJERCICIO 6 
SELECT INITCAP(C.NOMBRECLIENTE) AS "NOMBRE DE LOS CLIENTES", DP.CANTIDAD
FROM CLIENTES C, PEDIDOS P, DETALLEPEDIDOS DP
WHERE C.CODIGOCLIENTE = P.CODIGOCLIENTE
AND P.CODIGOPEDIDO = DP.CODIGOPEDIDO
AND DP.CANTIDAD > 200;

--EJERCICIO 7
SELECT C.NOMBRECLIENTE, E.NOMBRE||', '||E.APELLIDO1|| ' '||E.APELLIDO2 AS "REPRESENTANTE"
FROM CLIENTES C, EMPLEADOS E
WHERE C.CODIGOEMPLEADOREPVENTAS = E.CODIGOEMPLEADO;

--EJERCICIO 8
