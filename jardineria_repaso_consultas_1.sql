FROM CLIENTES C, EMPLEADOS E, PEDIDOS P
WHERE C.CODIGOEMPLEADOREPVENTAS = E.CODIGOEMPLEADO
AND C.CODIGOCLIENTE = P.CODIGOCLIENTE
AND P.COMENTARIOS IS NULL;